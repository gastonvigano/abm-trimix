
package Entidad;



public class Persona {
    int perId;
    String perApellido;
    String perNombre;
    int perFechaNacimiento;
    String perTipoDocumento;
    int perNumeroDocumeto;

    public Persona() {
    }

    public int getPerId() {
        return perId;
    }

    public void setPerId(int perId) {
        this.perId = perId;
    }

    public String getPerApellido() {
        return perApellido;
    }

    public void setPerApellido(String perApellido) {
        this.perApellido = perApellido;
    }

    public String getPerNombre() {
        return perNombre;
    }

    public void setPerNombre(String perNombre) {
        this.perNombre = perNombre;
    }

    public int getPerFechaNacimiento() {
        return perFechaNacimiento;
    }

    public void setPerFechaNacimiento(int perFechaNacimiento) {
        this.perFechaNacimiento = perFechaNacimiento;
    }

    public String getPerTipoDocumento() {
        return perTipoDocumento;
    }

    public void setPerTipoDocumento(String perTipoDocumento) {
        this.perTipoDocumento = perTipoDocumento;
    }

    public int getPerNumeroDocumeto() {
        return perNumeroDocumeto;
    }

    public void setPerNumeroDocumeto(int perNumeroDocumeto) {
        this.perNumeroDocumeto = perNumeroDocumeto;
    }
  
    
}